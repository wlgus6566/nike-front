-- 카테고리별 주문수
SELECT TP.CATEGORY2_CODE, COUNT(*)
FROM TB_ORDER TOS
LEFT JOIN TB_ORDER_PRODUCT_MAPPING TOPM
ON TOS.ORDER_SEQ = TOPM.ORDER_SEQ
LEFT JOIN TB_PRODUCT TP
ON TOPM.GOODS_SEQ = TP.GOODS_SEQ
# WHERE TOS.REGISTRATION_DT BETWEEN '2020-09-24 00:00:00' AND '2020-10-15 23:59:59'
# WHERE TOS.REGISTRATION_DT BETWEEN '2020-10-16 00:00:00' AND '2020-11-15 23:59:59'
WHERE TOS.REGISTRATION_DT BETWEEN '2020-11-16 00:00:00' AND '2020-12-15 23:59:59'
GROUP BY TP.CATEGORY2_CODE;
