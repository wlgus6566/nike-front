# -- 메뉴별 업로드 게시물(캠페인)수
SELECT
    T.MENU_CODE, COUNT(T.MENU_CODE) AS COUNT
    FROM
(
    SELECT CASE
               WHEN MENU_CODE = 'SP' THEN 'ASSET_SP'
               WHEN MENU_CODE = 'SU' THEN 'ASSET_SU'
               WHEN MENU_CODE = 'FA' THEN 'ASSET_FA'
               WHEN MENU_CODE = 'HO' THEN 'ASSET_HO'
               END AS MENU_CODE
    FROM TB_CONTENTS
#     WHERE REGISTRATION_DT BETWEEN '2020-12-16 00:00:00' AND '2021-01-15 23:59:59'
#     WHERE REGISTRATION_DT BETWEEN '2021-01-16 00:00:00' AND '2021-02-15 23:59:59'
    WHERE REGISTRATION_DT BETWEEN '2021-02-16 00:00:00' AND '2021-03-15 23:59:59'
    AND TOP_MENU_CODE = 'ASSET'
) T
GROUP BY T.MENU_CODE;
;

SELECT
    T.MENU_CODE, COUNT(T.MENU_CODE) AS COUNT
FROM
    (
        SELECT CASE
                   WHEN MENU_CODE = 'VMS' THEN 'VMS'
                   WHEN MENU_CODE = 'EKIN' THEN 'EKIN'
                   WHEN MENU_CODE = 'SOCIAL' THEN 'SOCIAL'
                   WHEN MENU_CODE = 'RB' THEN 'RB%'
                   END AS MENU_CODE
        FROM TB_CONTENTS
#         WHERE REGISTRATION_DT BETWEEN '2020-12-16 00:00:00' AND '2021-01-15 23:59:59'
#     WHERE REGISTRATION_DT BETWEEN '2021-01-16 00:00:00' AND '2021-02-15 23:59:59'
        WHERE REGISTRATION_DT BETWEEN '2021-02-16 00:00:00' AND '2021-03-15 23:59:59'
          AND TOP_MENU_CODE = 'TOOLKIT'
    ) T
GROUP BY T.MENU_CODE;
;


